    private void campoNomeD(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_campoNomeD
         //restrição de numero no  campo nome e da quantidade de caracteres
        String caracteres = "0987654321";
        if (caracteres.contains(evt.getKeyChar() + "")) {
            evt.consume();
        }
    }//GEN-LAST:event_campoNomeD

    private void campoTurno(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_campoTurno
          //restrição de numero no  campo turno e da quantidade de caracteres
        String caracteres = "0987654321";
        if (caracteres.contains(evt.getKeyChar() + "")) {
            evt.consume();
        }else if (txtTurnoD.getText().length() > 4) {
            evt.consume();
        }
    }//GEN-LAST:event_campoTurno

