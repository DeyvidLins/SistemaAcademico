    private void btnCadastraCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastraCursoActionPerformed
        // cadastrar disciplina no sqlServer
        DisciplinaEspecifica d = new DisciplinaEspecifica();
        try {
            if (txtNomeD.getText().equals("") == false) {
                String n = txtNomeD.getText();
            }
            d.setCodigoDisciplina(Integer.parseInt(txtCodigoD.getText()));
            d.setNomeDisciplina(txtNomeD.getText());
            d.setTurma(txtTurmaD.getText());
            d.setTurno(txtTurnoD.getText());

            NegocioDisciplinaEspecifica N = new NegocioDisciplinaEspecifica();
            N.cadastrarDisciplina(d);
            JOptionPane.showMessageDialog(rootPane, "Disciplina inserida no Sistema");

            //jLabel4.setText(String.valueOf(a.getMatricula()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_btnCadastraCursoActionPerformed

