    private void alterDadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterDadosActionPerformed
        //altera dados o aluno
        try {
            DisciplinaEspecifica c = new DisciplinaEspecifica();
            if (txtNomeD.getText().equals("") == false) {
                String n = txtNomeD.getText();
            }

            c.setCodigoDisciplina(Integer.parseInt(txtCodigoD.getText()));
            c.setNomeDisciplina(txtNomeD.getText());
            c.setTurma(txtTurmaD.getText());
            c.setTurno(txtTurnoD.getText());

            NegocioDisciplinaEspecifica N = new NegocioDisciplinaEspecifica();
            N.alterarDisciplina(c);
            JOptionPane.showMessageDialog(rootPane, "dados alterados ");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_alterDadosActionPerformed

