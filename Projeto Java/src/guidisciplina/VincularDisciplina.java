    private void btnListarDisciplinaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarDisciplinaActionPerformed
        try {
            DadosDisciplinaEspecifica dec = new DadosDisciplinaEspecifica();
            DisciplinaEspecifica c = new DisciplinaEspecifica();
            ArrayList<DisciplinaEspecifica> listadisciplina = dec.listar(c);

            DefaultTableModel modelo = new DefaultTableModel();
            String[] colunas = {"codigo", "Nome", "TURNO", "TURMA"};
            modelo.setColumnIdentifiers(colunas);
            for (DisciplinaEspecifica de : listadisciplina) {
                Object[] linha = {de.getCodigoDisciplina(), de.getNomeDisciplina(), de.getTurno(), de.getTurma()};
                modelo.addRow(linha);
            }

            NegocioDisciplinaEspecifica N = new NegocioDisciplinaEspecifica();
            this.listadisciplinas = N.listar(c);
            N.listar(c);

            tabDisciplina.setModel(modelo);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());

        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnListarDisciplinaActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (tabDisciplina.getSelectedRow() >= 0) {
            try {

                int remove = JOptionPane.showConfirmDialog(null, "VocÃª realmente Deseja remover a disciplina ? ", "ATENÃÃO", JOptionPane.YES_OPTION);
                if (remove == JOptionPane.YES_OPTION) {
                    DisciplinaEspecifica a = this.listadisciplinas.get(tabDisciplina.getSelectedRow());
                    NegocioDisciplinaEspecifica re = new NegocioDisciplinaEspecifica();
                    re.removerDisciplina(a);

                    JOptionPane.showConfirmDialog(rootPane, "disciplina removida com sucesso");
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(rootPane, ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "selecionar uma linha da tabela");
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            DisciplinaEspecifica a = new DisciplinaEspecifica();

            if (campoNomeC.getText().equals("") == false) {
                String n = campoNomeC.getText();
            }

            a.setCodigoDisciplina(Integer.parseInt(campoCodigoD.getText()));
            a.getCurso().setNomeCurso(campoNomeC.getText());

            NegocioDisciplinaEspecifica nde = new NegocioDisciplinaEspecifica();
            nde.vincularDisciplina(a);
            JOptionPane.showMessageDialog(rootPane, " disciplina vinculada ao curso ");
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }

    }//GEN-LAST:event_jButton2ActionPerformed

